// push Method => add to the end of Array
let cars = ["Audi","Civic","Mercedes","Supra"];
cars.push("Toyota");
document.write(`carsName = ${cars}`);

document.write("<br>");

// pop Method => delete from the end and returns it
let carsName = ["Audi","Civic","Mercedes","Supra"];
// delete from the end
carsName.pop("Toyota");
// add to the end
// carsName.push("BMW");
document.write(`carsName = ${carsName}`);
