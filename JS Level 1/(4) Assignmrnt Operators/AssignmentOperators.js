let b = 9;
let sum = a + b;
sum += 1; //Assignment Operator
let subtract = a - b;
subtract -= 1; //Assignment Operator
let multiplication = a * b;
multiplication *= 2; //Assignment Operator
let division = a / b;
division /= 2; //Assignment Operator
let modulo = a % b;
modulo %= 2; //Assignment Operator
let exponentiation = a **b;
exponentiation **= 2; //Assignment Operator

