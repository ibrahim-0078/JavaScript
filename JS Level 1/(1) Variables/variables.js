//var keyword
document.write("<h2>var Keyword</h2>");
var price1 = 70;
var price2 = 30;
totalPrice = price1 + price2;
document.write("price1 = " + price1);
document.write("<br>");
document.write("price2 = " + price2);
document.write("<br>");
document.write("totalPrice = " + totalPrice);
document.write("<br>");

//let keyword
document.write("<h2>let Keyword</h2>");
let a = 5;
b = 5;
mul = a * b;
document.write("mul = " + mul);
document.write("<br>");

// const keyword
document.write("<h2>const Keyword</h2>");
const pi = 3.14;
const rad = 4;
const area = pi * rad;
document.write("area = " + area);