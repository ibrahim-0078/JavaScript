// Traffic Light

let color = "green";

if(color === "red"){
    document.write(`Stop ,Light color is ${color}`);
}
else if(color === "yellow"){
    document.write(`slow down ,Light color is ${color}`);
}
else if(color === "green"){
    document.write(`Go ,Light color is ${color}`);
}
else{
    document.write(`Light is damage`);
}